# future_age_calculator.py
# This script calculates how old the user will be in 2050 based on their current age.

# Ask user for current age
current_age = int(input("How old are you? "))

# Calculate age in 2050
age_in_2050 = current_age + 27  # 2050 - 2023 = 27

# Print result
print(f"In 2050, you will be {age_in_2050} years old.")
